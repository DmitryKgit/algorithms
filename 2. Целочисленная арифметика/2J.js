// Функция возвращает сумму цифр числа a.
let a = String(198);
let limit = a.length;
let sum = 0;
for (let i = 0; i < limit; i++) {
    sum += Number(a.slice(i, i + 1));
    console.log("DEBUG", i, i + 1, sum);
}
console.log(sum);
