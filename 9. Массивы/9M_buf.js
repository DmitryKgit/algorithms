/*
9M.js 18.02.2020
Переставьте элементы данного списка в обратном порядке, затем выведите элементы полученного списка.
Эта задача отличается от предыдущей тем, 
что вам нужно изменить значения элементов самого списка, поменяв местами A[0] c A[n-1], A[1] с A[n-2], а затем вывести элементы списка подряд.
Входные данные
Вводится список чисел. Все числа списка находятся на одной строке.
Выходные данные
Выведите ответ на задачу.
Примеры
входные данные
1 2 3 4 5
выходные данные
5 4 3 2 1

Реверсирование (обращение) массива по месту. 
По времени O(n).
По памяти O(1).                                                                                                                      
*/

var arrNum = '1 2 3 4 5'.split(' ').map(Number);
var buf = 0;
for (var i = 0; i <= arrNum.length/2; i++) {
    buf = arrNum[i];
    arrNum[i] = arrNum[arrNum.length-1-i];
    arrNum[arrNum.length-1-i] = buf;
}
console.log(arrNum.map(String).join(' '));