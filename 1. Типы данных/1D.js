let num = 10; // Number(prompt('Введите количество членов ряда: '))
let summa = 0;
let limit = num * 2;
for (let i = 1; i <= limit; i += 4) {
    summa += 4/i - 4/(i + 2); 
    console.log("DEBUG", summa, i, i + 2);
}
console.log(summa);