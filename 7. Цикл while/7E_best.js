/*
7E.js 23.11.2019
По данному натуральному числу N выведите такое наименьшее целое число k, что 2**k >= N.
Операцией возведения в степень пользоваться нельзя!
Входные данные
Вводится натуральное число.
Выходные данные
Выведите ответ на задачу.
Примеры
входные данные
7
выходные данные
3
*/
var n = 511;
var pr = 2;
var count = 1;
while (pr < n) {
    count++;
    pr*=2;
}
console.log(count);