function IsPointInSquare(x, y) {
    return y <= -x + 1 && y <= x + 1 && y >= -x - 1 && y >= x - 1;
}

function autoTest() {
    console.log(IsPointInSquare(0, 0) ? "YES" : "NO");
    console.log(IsPointInSquare(0, 2) ? "YES" : "NO");
    console.log(IsPointInSquare(2, 0) ? "YES" : "NO");
    console.log(IsPointInSquare(-2, 0) ? "YES" : "NO");
    console.log(IsPointInSquare(0, -2) ? "YES" : "NO");
}

autoTest();



